package Sequencial.Conta;

public class Matematica {
    public static void main(String[] args) {
        double x = 3.0;
        double y = 4.0;
        double z = 5.0;
        double negative = -10.0;

        double raizQuadrada = Math.sqrt(y);
        double elevado = Math.pow(x, z);
        double absoluto = Math.abs(negative);

        System.out.println(raizQuadrada);
        System.out.println(elevado);
        System.out.println(absoluto);

    }
}
